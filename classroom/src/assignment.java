public class assignment {
    String id;
    String t_email;
    String title;
    String detail;
    String datetime;
    String course;
    String semester;
    String lastdate;
    String file;
    assignment(String id, String t_email, String title, String detail, String datetime, String course, String semester, String lastdate, String file) 
    {
        this.id=id;
        this.t_email=t_email;
        this.title=title;
        this.detail=detail;
        this.datetime=datetime;
        this.course=course;
        this.semester=semester;    
        this.lastdate=lastdate;
        this.file=file;
    }
}
