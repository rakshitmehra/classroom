public class courses {
    int id;
    String name;
    String dept_name;
    int semester;
    String description;
    public courses(int id, String name, String dept_name, int semester, String description)
    {
        this.id=id;
        this.name=name;
        this.dept_name=dept_name;
        this.semester=semester;
        this.description=description;
    }
}
