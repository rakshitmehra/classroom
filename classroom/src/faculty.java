public class faculty {
    String name;
    String father_name;
    String mobile ;
    String address;
    String qualification;
    String dept_name;
    String email;
    String photo;
    public faculty(String name, String father_name, String mobile, String address, String qualification, String dept_name, String email, String photo) {
        this.name = name;
        this.father_name = father_name;
        this.mobile = mobile;
        this.address = address;
        this.qualification = qualification;
        this.dept_name = dept_name;
        this.email = email;
        this.photo = photo;
    }
}
